// Lesson 02 — Variables Basics
// Declaring variables, logging them, and forcing an alert. Classic beginner suffering.

// Example of a mutable variable (you can reassign it)
let firstName = "Giannis";

// Constant stays loyal to its value, unlike let
const lastName = "Papajavascriptidis";

// Numbers exist too
let age = 31;

// Print values to the console for debugging or boredom
console.log(`Name: ${firstName}, Age: ${age}`);

// Repetition just to show you can
console.log(age);

// Show a pop-up because web devs love pain
alert(`${firstName} ${lastName}`);

